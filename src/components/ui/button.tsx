import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-xl text-sm font-semibold ring-offset-background transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90 shadow-[0_4px_0_hsl(var(--primary)/0.4),0_6px_12px_hsl(var(--secondary)/0.2)] hover:shadow-[0_6px_0_hsl(var(--primary)/0.4),0_10px_20px_hsl(var(--secondary)/0.3)] hover:-translate-y-0.5 active:translate-y-0.5 active:shadow-[0_2px_0_hsl(var(--primary)/0.4),0_4px_8px_hsl(var(--secondary)/0.2)]",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90 shadow-[0_4px_0_hsl(var(--destructive)/0.4),0_6px_12px_hsl(var(--secondary)/0.2)] hover:shadow-[0_6px_0_hsl(var(--destructive)/0.4),0_10px_20px_hsl(var(--secondary)/0.3)] hover:-translate-y-0.5 active:translate-y-0.5",
        outline: "border-2 border-input bg-background hover:bg-accent/10 hover:border-primary/50 shadow-[0_2px_4px_hsl(var(--secondary)/0.1)] hover:shadow-[0_4px_8px_hsl(var(--secondary)/0.15)] hover:-translate-y-0.5",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80 shadow-[0_4px_0_hsl(var(--secondary)/0.3),0_6px_12px_hsl(var(--secondary)/0.2)] hover:-translate-y-0.5",
        ghost: "hover:bg-accent/10 hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
      },
      size: {
        default: "h-11 px-5 py-2",
        sm: "h-9 rounded-lg px-4",
        lg: "h-12 rounded-xl px-10 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
